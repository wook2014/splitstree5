package splitstree5.core.datablocks.view;

import javafx.geometry.Point2D;
import javafx.scene.Node;

/**
 * node view
 * Daniel Huson, 10.2107
 */
public class ANodeView {
    private Node shape;
    private Node label;
    private Point2D location;

    private final jloda.graph.Node v;

    public ANodeView(jloda.graph.Node v) {
        this.v = v;
    }

    public void setShape(Node shape) {
        this.shape = shape;
    }

    public Node getShape() {
        return shape;
    }

    public void setLabel(Node label) {
        this.label = label;
    }

    public Node getLabel() {
        return label;
    }

    public Point2D getLocation() {
        return location;
    }

    public void setLocation(Point2D location) {
        this.location = location;
    }

    public jloda.graph.Node getV() {
        return v;
    }
}
