package splitstree5.main.graphtab.base;

import javafx.scene.layout.Pane;

public class Presenter {
    final double SCALE_DELTA = 1.1;

    private final GraphTab graphTab;

    public Presenter(GraphTab graphTab) {
        this.graphTab = graphTab;
    }

    public void setup(Pane pane) {
        pane.setOnScroll(event -> {
            event.consume();

            if (graphTab.getLayout() == GraphLayout.Radial) {
                final double scaleFactor = (event.getDeltaY() > 0) ? SCALE_DELTA : 1 / SCALE_DELTA;
                graphTab.scale(scaleFactor, scaleFactor);
            } else {
                if (Math.abs(event.getDeltaY()) > Math.abs(event.getDeltaX())) {
                    final double scaleFactor = (event.getDeltaY() > 0) ? SCALE_DELTA : 1 / SCALE_DELTA;
                    graphTab.scale(1, scaleFactor);
                } else {
                    final double scaleFactor = (event.getDeltaX() > 0) ? SCALE_DELTA : 1 / SCALE_DELTA;
                    graphTab.scale(scaleFactor, 1);
                }
            }
        });
    }
}
